package javamocks.t01.q28;

public class Certification {
	
	private String name;
	private double price;
	
	public Certification(String s, double d) {
		this.name = s;
		this.price = d;
	}

	public String getName() {
		return name;
	}



	public double getPrice() {
		return price;
	}
}
